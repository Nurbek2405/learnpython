class Shape:
    def __init__(self, a, b, c):
        self.a = a
        self.b = b
        self.c = c

    def get_area(self):
        return "–ù–µ–æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–∞—è –ø–ª–æ—â–∞–¥—å"

    def get_perimeter(self):
        return "–ù–µ–æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω—ã–π –ø–µ—Ä–∏–º–µ—Ç—Ä"

class Triangle(Shape):
    def __init__(self, a, b, c):
        super().__init__(a,b,c)

    def get_perimeter(self):
        return self.a+self.b+self.c

    def get_area(self):
        s = self.get_perimeter() / 2
        return math.sqrt(s*(s-self.a)*(s-self.b)*(s-self.c))

class Square(Shape):
    def __init__(self, side):
        self.side = side

    def get_perimeter(self):
        return 4 * self.side

    def get_area(self):
        return self.side ** 2

triangle = Triangle(3, 4, 5)
